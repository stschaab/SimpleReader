// ── Validierung ──
function validate(id, level, text, original) {
  const errors = [];
  // Erlaubt: kyrillisch, Interpunktion, Ziffern, Leerzeichen, Klammern, Anführungszeichen
  // Verboten: lateinische Buchstaben, CJK
  if (/[A-Za-z\u4e00-\u9fff]/.test(text)) {
    errors.push("enthält lateinische/chinesische Zeichen!");
  }
  // B2 sollte kürzer sein als Original
  if (level === "B2") {
    const origWords = original.split(/\s+/).length;
    const newWords = text.split(/\s+/).length;
    if (newWords > origWords) {
      errors.push("B2 ist LÄNGER als Original (" + newWords + " > " + origWords + " Wörter)");
    }
  }
  return errors;
}

// ── Main ──
const dataPath = path.join(__dirname, "..", "data.js");
const book = require(dataPath);

let applied = 0;
let errors = 0;

Object.keys(SIMPLIFICATIONS).forEach((idStr) => {
  const id = parseInt(idStr, 10);
  const p = book.paragraphs.find((x) => x.id === id);
  if (!p) {
    console.log("✗ ID " + id + " nicht gefunden!");
    errors++;
    return;
  }
  ["C1", "B2"].forEach((level) => {
    const text = SIMPLIFICATIONS[idStr][level];
    if (!text || !text.trim()) return;
    const errs = validate(id, level, text, p.original);
    if (errs.length) {
      console.log("✗ ID " + id + " " + level + ": " + errs.join("; "));
      errors++;
    } else {
      p[level] = text;
      applied++;
    }
  });
});

if (errors > 0) {
  console.log("\n❌ " + errors + " Fehler. data.js NICHT geschrieben.");
  process.exit(1);
}

